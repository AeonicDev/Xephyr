package com.dhlab.xephyr.bukkit.jsonadapter.itemstack;

/**
 * All of the required Strings to serialize and deserialize ItemStacks.
 *
 * @author maladr0it
 */
public final class ItemStackFormat {

    /**
     * The item ID.
     */
    public static final String ID = "id";

    /**
     * The item amount.
     */
    public static final String COUNT = "count";

    /**
     * The item damage value.
     */
    public static final String DAMAGE = "damage";

    /**
     * The item name.
     */
    public static final String NAME = "name";

    /**
     * The item lore.
     */
    public static final String LORE = "lore";

    /**
     * The item enchants.
     */
    public static final String ENCHANTS = "enchants";

    /**
     * The item repair cost.
     */
    public static final String REPAIRCOST = "repaircost";

    /**
     * The book title.
     */
    public static final String BOOK_TITLE = "book-title";

    /**
     * The book author.
     */
    public static final String BOOK_AUTHOR = "book-author";

    /**
     * The book pages.
     */
    public static final String BOOK_PAGES = "book-pages";

    /**
     * The armor color.
     */
    public static final String LEATHER_ARMOR_COLOR = "color";

    /**
     * The map scaling.
     */
    public static final String MAP_SCALING = "map-scaling";

    /**
     * The skull owner.
     */
    public static final String SKULL_OWNER = "skull";

    /**
     * The potion effects.
     */
    public static final String POTION_EFFECTS = "potion-effects";

    /**
     * The firework effect.
     */
    public static final String FIREWORK_EFFECT = "firework-effect";

    /**
     * The firework effects.
     */
    public static final String FIREWORK_EFFECTS = "firework-effects";

    /**
     * The firework power.
     */
    public static final String FIREWORK_POWER = "firework-power";

    /**
     * The item's stored enchants.
     */
    public static final String STORED_ENCHANTS = "stored-enchants";

}
